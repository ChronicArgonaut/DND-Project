MERGE MonsterStats AS M
USING MonsterBase AS B
ON (B.MonsterID = M.MonsterID)
WHEN NOT MATCHED THEN
	Insert(MonsterID) Values (B.MonsterID);

MERGE MonsterStats AS M
USING #Temp2 AS T2
ON (T2.MonsterID = M.MonsterID)
when matched then
	Update set M.STR=t2.STR,M.DEX=t2.DEX,M.CON=t2.CON,M.WIS=t2.WIS,M.CHA=t2.CHA,M.int=t2.int;

MERGE MonsterLocations AS M
USING MonsterBase AS B
ON (B.MonsterID = M.MonsterID)
WHEN Not MATCHED THEN
	Insert(MonsterID) Values (B.MonsterID);


MERGE MonsterLocations AS M
USING #temp1 AS t
ON (t.MonsterID = M.MonsterID)
WHEN MATCHED THEN
	Update Set m.arctic=t.arctic,m.coast=t.coast,m.desert=t.desert,m.forest=t.forest,m.grassland=t.grassland,m.hill=t.hill,m.mountain=t.mountain,m.swamp=t.swamp,m.underdark=t.underdark,m.underwater=t.underwater,m.urban=t.urban ;

MERGE MonsterAttacks AS M
