MERGE MonsterBase AS B
USING #Temp1 AS t1
ON (B.name = T1.name)
WHEN NOT MATCHED THEN
    INSERT (MonsterID, Name, Type, ALIGNMENT, Size, CR, AC, HP, [Spellcasting?],page,book) VALUES (newid(), t1.Name, t1.Type, t1.ALIGNMENT, t1.Size, t1.CR, t1.AC, t1.HP, t1.[Spellcasting?],t1.page,t1.book);

MERGE MonsterBase AS B
USING #Temp2 AS t2
ON (B.name = T2.name)
WHEN NOT MATCHED THEN
    INSERT (MonsterID, Name) VALUES (newid(), t2.Name);

MERGE #Temp2 AS T2
USING MonsterBase AS B
ON (B.name = T2.name)
WHEN MATCHED THEN
    Update set T2.MonsterID=B.MonsterID;

MERGE #Temp1 AS T1
USING MonsterBase AS B
ON (B.name = T1.name)
WHEN MATCHED THEN
    Update set T1.MonsterID=B.MonsterID;

